    class KA {
        public Queue<int> input(Node node, Queue<int> ins) {
            Queue<int> output = new Queue<int>();

            while (ins.Count > 0) {
                (Node, int) a = node.process(ins.Dequeue());
                node = a.Item1;
                output.Enqueue(a.Item2);
            }

            return output;
        }
    }
