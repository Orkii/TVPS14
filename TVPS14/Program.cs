        static bool diff(string[] mass) {
            int count = 1;

            for (int elemm = 0; elemm < mass.Length - 1; elemm++) {
                for (int elempm = elemm + 1; elempm < mass.Length; elempm++) {
                    if (mass[elemm] == mass[elempm])
                        break;

                    if (elempm == mass.Length - 1)
                        count++;
                }
            }
            if (count == 5) return true;
            return false;
        }


